     * @param $id
     * @return \Illuminate\Http\Response
     */
    public function index($id)
    {
        $product = Product::findOrFail($id);

        $buyers = $product->transactions()
                                        ->with('buyer')
                                        ->get()
                                        ->pluck('buyer')
                                        ->unique('id')
                                        ->values();

        return $this->showAll($buyers);

    }

