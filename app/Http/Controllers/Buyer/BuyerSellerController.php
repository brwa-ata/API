    {
        $buyer = Buyer::findOrFail($id);
        $seller = $buyer->transactions()->with('product.seller')
            ->get()
            ->pluck('product.seller')
            ->unique('id')  // ama bo awaya ka sellery dwbara pshan nayat
            ->values();       // amash bo awaya bo aw sellera dwbarayay ka pshan nadre valuey null pshan nayat

        return $this->showAll($seller);
    }

